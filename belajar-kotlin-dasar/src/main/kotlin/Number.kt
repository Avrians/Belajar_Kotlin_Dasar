fun main() {

    var age : Int = 200

    var sample: Float = 10.10F

    println(age)
    println(sample)

    var binary: Int = 0b010101010
    println(binary)

    var harga: Long = 9000_000_000L
    println(harga)

    var hargaInt : Int = harga.toInt()
    println(hargaInt)

    var doubleBinary: Double = binary.toDouble()
    println(doubleBinary)
}